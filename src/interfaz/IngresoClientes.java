    private void ingresoCliente() {
        Conexion cc = new Conexion();
        Connection cn = cc.conectar();
        String cedula = jTxtCedula.getText();
        String nombre = jTxtNombre.getText();
        String apellido = jTxtApellido.getText();
        String direccion = jTxtDireccion.getText();
        String telefono = jTxtTelefono.getText();
        if (direccion.isEmpty()) {
            direccion = "Sin direccion";
        }
        if (telefono.isEmpty()) {
            telefono = "0000000000";
        }
        if (cedula.isEmpty() || nombre.isEmpty() || apellido.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Existen campos no llenos o invÃ¡lidos", "Error al registrar Cliente", JOptionPane.ERROR_MESSAGE);
        } else {
            try {
                String sql = "";
                sql = "INSERT INTO CLIENTES (ID_CLI, NOM_CLI, APE_CLI, DIR_CLI, TEL_CLI)VALUES(?,?,?,?,?)";
                PreparedStatement psd = cn.prepareStatement(sql);
                psd.setString(1, cedula);
                psd.setString(2, nombre);
                psd.setString(3, apellido);
                psd.setString(4, direccion);
                psd.setString(5, telefono);
                int n = psd.executeUpdate();
                if (n > 0) {
                    JOptionPane.showMessageDialog(null, "Se ha registrado el cliente exitosamente", "Registro de Cliente", JOptionPane.INFORMATION_MESSAGE);
                    cargarTablaClientes();
                    habilitarBtnInicio();
                }
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "Ya existe un cliente con dicha cedula", "Error al registrar Cliente", JOptionPane.ERROR_MESSAGE);
            }
        }

    }

    private void cargarTablaClientes() {
        try {
            Conexion cc = new Conexion();
            Connection cn = cc.conectar();
            String[] encabezado = {"Cedula", "Nombre", "Apellido", "Direccion", "Telefono"};
            jTblClientes.setModel(new DefaultTableModel(null, encabezado) {
                public boolean isCellEditable(int row, int column) {
                    return false;
                }
            });
            String datos[] = new String[5];
            DefaultTableModel model = (DefaultTableModel) jTblClientes.getModel();
            String sql = "";
            sql = "SELECT * FROM CLIENTES";
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            while (rs.next()) {
                datos[0] = rs.getString("ID_CLI");
                datos[1] = rs.getString("NOM_CLI");
                datos[2] = rs.getString("APE_CLI");
                datos[3] = rs.getString("DIR_CLI");
                datos[4] = rs.getString("TEL_CLI");
                model.addRow(datos);
            }
            jTblClientes.setModel(model);
        } catch (SQLException ex) {
            ex.printStackTrace();
        }

    }


